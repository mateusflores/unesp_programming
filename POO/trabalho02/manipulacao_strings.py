# Concatenação com +
str1 = "Batata " + "doce"
print(str1)

# Obtendo o tamanho da String com len()
str2 = "Universidade Estadual Paulista"
print(len(str2))

# Trocando parte da String com replace()
print(str2.replace("Paulista", "Carioca"))

# Dividindo string com split()
print(str2.split(" "))

# Letras maiúsculas com .upper()
print(str2.upper())

# Obtendo a posição de uma substring
print(str2.find("Estadual"))
